# for list
lucky_numbers = [1, 2, 3]
for number in lucky_numbers:
    print(number)

# for string
my_name = "Ayar Hlaine"
for character in my_name:
    print(character)

# for range
for index in range(1, 10):
    print(index)

# looping array with range
for index in range(len(lucky_numbers)):
    print(lucky_numbers[index])
